# ==========================================
# 1. 변수와 데이터 타입
# ==========================================

# 1.1 정수형 (int): 음수, 양수 모두 사용할 수 있습니다.
x = 10

# 1.2 실수형 (float): 소수점이 있는 숫자로 정밀한 계산에 유용합니다.
y = 5.3

# 1.3 문자열 (str): 문자들의 집합이며 따옴표를 사용합니다.
z = "안녕하세요"
name = "성호"

# 문자열 포매팅: f-string을 사용하여 문자열 사이에 변수를 넣습니다.
hello = f"안녕 내 이름은 {name}이야."
print(hello)

# 1.4 불리언 (bool): True 또는 False 두 가지 값만 가질 수 있습니다.
a = True
print(type(a))

# 1.5 리스트 (list): 여러 항목을 하나에 저장하며 대괄호[]를 사용합니다.
BND = ["성호", "리우", "재현"]
print(BND)

# 1.6 튜플 (tuple): 리스트와 달리 값을 변경할 수 없으며 소괄호()를 사용합니다.
color = ("red", "blue", "green")

# 1.7 딕셔너리 (dict): 키(Key)와 값(Value)이 쌍으로 이루어진 타입입니다.
BND_age = {"성호": 22, "태산": 21}
print(BND_age["성호"])

# 1.8 집합 (set): 중복되지 않는 고유한 값들의 모음입니다.
age = {1, 2, 3, 4, 5}

# 1.9 None: 아무런 값도 가지지 않는 상태를 의미합니다.
j = None

# 타입 변환 (Casting)
h = 15
p = str(h)    # 숫자를 문자열로 ("15")
print(type(p))

gr = 10
t = float(gr) # 정수를 실수로 (10.0)
print(t)


# ==========================================
# 2. 연산자 (Operators)
# ==========================================

# 2.0 기본 산술 연산자 예제
a = 15
b = 4

print(a + b)   # 덧셈: 19
print(a - b)   # 뺄셈: 11
print(a * b)   # 곱셈: 60
print(a / b)   # 나눗셈: 3.75
print(a // b)  # 몫: 3 (소수점 이하 버림)
print(a % b)   # 나머지: 3
print(a ** b)  # 제곱: 50625 (15의 4승)

# 2.1 비교 연산자: 두 값을 비교하여 True/False를 반환합니다.
c = 5
m = 6

print(c == m)  # 두 값이 같은지 비교
print(c != m)  # 두 값이 다른지 비교
print(c < m)   # c가 m보다 작은가?
print(c <= m)  # c가 m보다 작거나 같은가?

# 2.2 논리 연산자
ha = True
ah = False

print(ha and ah) # 둘 다 참일 때만 True
print(ha or ah)  # 둘 중 하나만 참이어도 True
print(not ha)    # 값을 반대로 뒤집음

# 2.3 연산자 우선순위
