#2.1 비교 연산자
c=5
m=6
print(c==m)
#두 값이 같은지 비교
print(c!=m)
#두 값이 다른지 비교
print(c<m)
print(c<=m)
#2.2 논리 연산자
ha=True
ah=False
print(ha and ah)
print(ha or ah)
print(not ha)
